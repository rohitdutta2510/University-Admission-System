package UAS.utils;


public class StudentAccountInfo {
    public String name;
    public String emailID;
    public String password;
    public String appNo;
    
    public StudentAccountInfo(String name, String emailID, String password, String appNo){
        this.name = name;
        this.password = password;
        this.emailID = emailID;
        this.appNo = appNo;
    }
}
